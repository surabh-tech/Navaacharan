        <script>
        $(function () {
            $('#contactForm').on('submit', function (e) {
                e.preventDefault();
                var $form   = $(this);
                var $btn    = $('#mail_button');
                var $result = $('#form-result');

                var name  = $.trim($('#name').val());
                var email = $.trim($('#email').val());

                if (!name || !email) {
                    $result.removeClass('d-none alert-success').addClass('alert-danger')
                           .text('Please fill in your name and email address.');
                    return;
                }

                $btn.prop('disabled', true);
                $result.addClass('d-none');

                $.ajax({
                    url: 'email-templates/contact-form.php',
                    method: 'POST',
                    data: $form.serialize(),
                    dataType: 'json',
                    success: function (res) {
                        $result.removeClass('d-none');
                        if (res.alert && res.alert.indexOf('success') !== -1) {
                            $result.removeClass('alert-danger').addClass('alert-success')
                                   .text(res.message || 'Your message has been sent successfully!');
                            $form[0].reset();
                        } else {
                            $result.removeClass('alert-success').addClass('alert-danger')
                                   .text(res.message || 'Could not send message. Please email us directly.');
                        }
                    },
                    error: function () {
                        $result.removeClass('d-none alert-success').addClass('alert-danger')
                               .text('An error occurred. Please email amey.kulkarni@navaacharan.com directly.');
                    },
                    complete: function () {
                        $btn.prop('disabled', false);
                    }
                });
            });
        });
        </script>
